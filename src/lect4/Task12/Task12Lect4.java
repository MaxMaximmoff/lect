package lect4.Task12;

/*
    Задание 12: 10 чисел

    Вывести на экран числа от 1 до 10, используя цикл while.
*/


public class Task12Lect4 {

    public static void main(String[] args) throws Exception {

        int n = 1;
        while (n<11){
            System.out.print(n + " ");
            n++;
        }

    }
}
